    def authenticate(self, email='me@email.com', password='password'):
        user = self.query("SELECT * FROM users WHERE email = %s", (email,))

        if not user:
            return {'success': 0, 'message': 'User not found'}
        
        encryptPass = self.onewayEncrypt(password)
        if user[0]['password'] == encryptPass:
            return {'success':1, 'message': 'Authentication succes, logging in'}
        else:
            return {'success': 0, 'message': 'Incorrect password'}
        
    def onewayEncrypt(self, string):
        encrypted_string = hashlib.scrypt(string.encode('utf-8'),
                                          salt = self.encryption['oneway']['salt'],
                                          n    = self.encryption['oneway']['n'],
                                          r    = self.encryption['oneway']['r'],
                                          p    = self.encryption['oneway']['p']
                                          ).hex()
        return encrypted_string


    def reversibleEncrypt(self, type, message):
        fernet = Fernet(self.encryption['reversible']['key'])
        
        if type == 'encrypt':
            message = fernet.encrypt(message.encode())
        elif type == 'decrypt':
            message = fernet.decrypt(message).decode()

        return message
